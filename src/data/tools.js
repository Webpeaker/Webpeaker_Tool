export const allTools = Object.entries(groups).flatMap(([categoryId, names]) =>
  names.map((name) => {
    const id = slugify(name);
    const meta = categoryMeta[categoryId];

    return {
      id,
      name,
      path: implementedPaths[id] || `/tools/${id}`,
      categoryId,
      category: meta.name,
      icon: iconForTool(name, categoryId),
      color: meta.color,
      bg: meta.bg,
      description: descriptions[categoryId],
      isPopular: ['word-character-counter', 'json-formatter-and-validator', 'password-generator', 'qr-code-customizer', 'base64-encode-decode', 'color-picker', 'text-formatter', 'calculator'].includes(id),
    };
  }),
);

export const categories = Object.entries(categoryMeta).map(([id, meta]) => ({
  id,
  ...meta,
  count: `${groups[id].length} Tools`,
}));

export const tools = allTools;
