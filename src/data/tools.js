const iconForTool = (name, categoryId) => {
  const lower = name.toLowerCase();
  if (lower.includes('password') || lower.includes('token') || lower.includes('jwt')) return KeyRound;
  if (lower.includes('hash') || lower.includes('checksum')) return Hash;
  if (lower.includes('json')) return FileJson;
  if (lower.includes('url') || lower.includes('link')) return Link2;
  if (lower.includes('chart') || lower.includes('statistics')) return BarChart3;
  if (lower.includes('domain') || lower.includes('ssl') || lower.includes('dns') || lower.includes('ip')) return Globe2;
  return categoryMeta[categoryId].icon;
};

